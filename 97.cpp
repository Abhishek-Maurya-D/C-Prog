// Library functions sqrt(), cbrt(), min(), max() & pow()

#include<iostream>
#include<cmath>
using namespace std;
int main()
{
    cout<<sqrt(65)<<"\n"; // library function
    cout<<cbrt(65)<<"\n"; // library function
    cout<<min(4,8)<<"\n"; // library function
    cout<<max(4,8)<<"\n"; // library function
    cout<<pow(4,2)<<"\n"; // library function
}